let currentLang = 'en';

function applyTranslations() {
    const t = translations[currentLang];

    // Text content
    document.querySelectorAll('[data-t]').forEach(el => {
        const key = el.getAttribute('data-t');
        if (t[key] !== undefined) el.textContent = t[key];
    });

    // Placeholder attributes
    document.querySelectorAll('[data-t-placeholder]').forEach(el => {
        const key = el.getAttribute('data-t-placeholder');
        if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
    });

    // Direction & lang attribute
    document.documentElement.setAttribute('lang', currentLang);
    document.documentElement.setAttribute('dir', currentLang === 'ar' ? 'rtl' : 'ltr');

    // Toggle button label
    document.getElementById('langToggle').textContent = currentLang === 'en' ? 'AR' : 'EN';
}

document.getElementById('langToggle').addEventListener('click', () => {
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    applyTranslations();
});

// ===================== Portfolio Filtering =====================
function initPortfolioFilters() {
    const filterBtns = document.querySelectorAll('.filter-btn');
    const items = document.querySelectorAll('.portfolio-item');

    filterBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            // Remove active class
            filterBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            const filter = btn.getAttribute('data-filter');

            items.forEach(item => {
                // Apply a small timeout for a smoother reflow transition
                item.style.opacity = '0';
                item.style.transform = 'scale(0.95)';
                
                setTimeout(() => {
                    if (filter === 'all' || item.getAttribute('data-category') === filter) {
                        item.style.display = 'block';
                        setTimeout(() => {
                            item.style.opacity = '1';
                            item.style.transform = 'scale(1)';
                        }, 50);
                    } else {
                        item.style.display = 'none';
                    }
                }, 300);
            });
            
            // Re-trigger observer for visible items if needed
            setTimeout(() => {
                 ScrollTrigger.refresh(); // if using external libs, else just let layout shift handle it
            }, 350);
        });
    });
}


// ===================== Scroll Animations =====================
const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            observer.unobserve(entry.target);
        }
    });
}, { threshold: 0.1 });

// Added portfolio classes
function observeElements() {
    document.querySelectorAll('.service-card, .process-step, .about-grid, .contact-grid, section, .portfolio-item').forEach(el => {
        el.classList.add('animate-on-scroll');
        observer.observe(el);
    });
}


// ===================== Active nav link =====================
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-links a[href^="#"]');

window.addEventListener('scroll', () => {
    let current = '';
    sections.forEach(section => {
        const sectionTop = section.offsetTop - 120;
        if (window.scrollY >= sectionTop) {
            current = section.getAttribute('id');
        }
    });
    navLinks.forEach(link => {
        link.classList.remove('active');
        if (link.getAttribute('href') === `#${current}`) {
            link.classList.add('active');
        }
    });
});

